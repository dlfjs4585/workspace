package 세번째_연산자;

public class 연산자_1 {
    public static void main(String[] args) {
        //산술연산자 +, -, *, /, %(나머지)
        // % mod 연산자
        System.out.println(10 / 4);
        System.out.println(10 % 4);
        System.out.println(10 % 3);

        //비교연산자 >, <. >=, <=, !=, ==
        System.out.println(5 >= 3);

        //논리 연산자(맞다(참), 틀리다(거짓))
        //&& 그리고(and), || 그러나(or)
        int x = 7;
        boolean r1 = x > 5 && x <= 10;



    }
}

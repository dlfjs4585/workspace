package 세번째_연산자;

public class 초등상수문제 {
    public static void main(String[] args) {
        int pencils = 534;
        int stu = 30;

        int pencilsPerStudent = pencils / stu;
        System.out.println(pencilsPerStudent);
        int pencilsLeft = pencils % stu;
        System.out.println(pencilsLeft);
    }
}
